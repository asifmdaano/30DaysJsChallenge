function addClass() {
    // select the element.
    const ele = document.getElementById('task8')

    // add the 'newClass' class to the element.
    ele.classList.add('newClass');
}

const removeClass = () => {
    // select the element.
    let element = document.getElementById("task8");

    // remove the 'newClass' class to the element.
    element.classList.remove('newClass');
};